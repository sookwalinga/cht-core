    NULLIF(NULLIF(doc #>> '{fields,pregnancy_months_left}', 'NaN'), '')::INTEGER AS pregnancy_months_left,
    NULLIF(doc #>> '{fields,wash,subgroup_cleanliness,hand_wash_importance}','')::BOOLEAN  AS is_hand_wash_important,
    NULLIF(doc #>> '{fields,wash,subgroup_health_concerns,health_concerns}','') AS selected_health_concerns,
    NULLIF(doc #>> '{fields,llints,mosquito_net_qn,has_mosquito_net}', '')::BOOLEAN AS has_mosquito_net,
    NULLIF(doc #>> '{fields,quality_of_survery_care,consent_for_questions}', '')::BOOLEAN AS consent_for_questions,
    doc #>> '{fields,quality_of_survery_care,facility_island}' AS facility_island,
    doc #>> '{fields,quality_of_survery_care,facility_district}' AS facility_district,
